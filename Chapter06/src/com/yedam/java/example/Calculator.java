package com.yedam.java.example;

public class Calculator {
	
	double plus(double x, double y) {
		double result;
		result = x + y;
		return result;
	}
	
	double minus(double x, double y) {
		double result;
		result = x - y;
		return result;
	}
	
	double multiply(double x, double y) {
		double result;
		result = x * y;
		return result;
	}
	
	double divide(double x, double y) {
		double result;
		result = x / y;
		return result;
	}

}
